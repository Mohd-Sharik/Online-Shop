package com.onlineShop.example.commonModel;

public class PaginationAttribute {
	
	private Integer pageIndex;
	private Integer totalPages;
	private Long totalRecords;
	private Integer showRecordOnOnePage;
	private Integer fetchrecords;
	
	
	
	
	
	public Integer getPageIndex() {
		return pageIndex;
	}
	public void setPageIndex(Integer pageIndex) {
		this.pageIndex = pageIndex;
	}
	public Integer getTotalPages() {
		return totalPages;
	}
	public void setTotalPages(Integer totalPages) {
		this.totalPages = totalPages;
	}
	public Long getTotalRecords() {
		return totalRecords;
	}
	public void setTotalRecords(Long totalRecords) {
		this.totalRecords = totalRecords;
	}
	public Integer getShowRecordOnOnePage() {
		return showRecordOnOnePage;
	}
	public void setShowRecordOnOnePage(Integer showRecordOnOnePage) {
		this.showRecordOnOnePage = showRecordOnOnePage;
	}
	public Integer getFetchrecords() {
		return fetchrecords;
	}
	public void setFetchrecords(Integer fetchrecords) {
		this.fetchrecords = fetchrecords;
	}
	
	
	
	

}
